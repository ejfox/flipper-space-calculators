// draw_phase_view — Planetary alignment diagnostic.
//
// This is the view for travelers who want to understand WHY a window
// is good or bad. It shows three numbers and a visual diagram:
//
// Text (left side):
//   NOW:   +60   — Current phase angle (destination degrees from Earth)
//   IDEAL: +44   — Where the destination SHOULD be for optimal transfer
//   ERROR: +16   — How far off we are (NOW minus IDEAL)
//
// Diagram (right side):
//   A circle representing the orbital plane, viewed from above.
//   - Filled dot "E" at 0° (right) = Earth (your position)
//   - Hollow circle = ideal destination position for launch
//   - Filled dot = actual destination position right now
//
// When the filled dot overlaps the hollow circle, you're in the window.
// As you scroll days with LEFT/RIGHT, watch the filled dot approach
// the hollow circle. When they converge, that's your launch date.
//
// Pro tip: on the phase view, long-press OK to snap directly to the
// day when the filled dot hits the hollow circle. Then switch to DATA
// view to confirm the delta-v is within your fuel budget.
static void draw_phase_view(Canvas* canvas, AppState* state) {
    canvas_clear(canvas);
    canvas_set_color(canvas, ColorBlack);

    const CelestialBody* dest = &destinations[state->current_destination];
    canvas_set_font(canvas, FontPrimary);
    canvas_draw_str(canvas, 2, 10, "PHASE");

    canvas_set_font(canvas, FontSecondary);
    canvas_draw_str(canvas, 40, 10, dest->name);

    char line[32];

    snprintf(line, sizeof(line), "NOW:   %+.0f", state->current_transfer.current_phase_deg);
    canvas_draw_str(canvas, 2, 24, line);

    snprintf(line, sizeof(line), "IDEAL: %+.0f", state->current_transfer.ideal_phase_deg);
    canvas_draw_str(canvas, 2, 34, line);

    double err = state->current_transfer.current_phase_deg -
                 state->current_transfer.ideal_phase_deg;
    while(err > ONE_EIGHTY)
        err -= THREE_SIXTY;
    while(err < -ONE_EIGHTY)
        err += THREE_SIXTY;
    snprintf(line, sizeof(line), "ERROR: %+.0f", err);
    canvas_draw_str(canvas, 2, 44, line);

    // Visual alignment diagram
    const int cx = 96;
    const int cy = 42;
    const int r = 18;

    canvas_draw_circle(canvas, cx, cy, r);

    // Earth at 0° (right side of circle)
    canvas_draw_disc(canvas, cx + r, cy, 2);
    canvas_draw_str(canvas, cx + r - 2, cy - 5, "E");

    // Ideal position — hollow circle (this is where the destination SHOULD be)
    double ideal_rad = state->current_transfer.ideal_phase_deg * PI_OVER_180;
    int ix = cx + (int)((double)r * d_cos(ideal_rad));
    int iy = cy - (int)((double)r * d_sin(ideal_rad));
    canvas_draw_circle(canvas, ix, iy, 2);

    // Actual position — filled circle (this is where the destination IS)
    double actual_rad = state->current_transfer.current_phase_deg * PI_OVER_180;
    int ax = cx + (int)((double)r * d_cos(actual_rad));
    int ay = cy - (int)((double)r * d_sin(actual_rad));
    canvas_draw_disc(canvas, ax, ay, 2);

    // Current date
    format_date(line, sizeof(line), state->current_day);
    canvas_draw_str(canvas, 2, 62, line);
}

