// calculate_orbital_position — Where is this body in its orbit right now?
//
// Returns the angular position (radians, 0 to 2*pi) of a celestial body
// on a given day of the year. Uses mean anomaly (assumes circular orbits).
//
// As a traveler, you don't call this directly — it feeds into the transfer
// calculator and the orbit diagram. But conceptually: if you're looking at
// the orbit view and Mars is at the "2 o'clock" position, this function
// computed that angle.
//
// Example:
//   Earth on day 0   → 0.0 rad (at its epoch starting position)
//   Earth on day 91  → ~π/2 rad (quarter orbit, roughly spring equinox)
//   Mars on day 0    → 0.524 rad (30° starting offset)
static double calculate_orbital_position(const CelestialBody* body, int day_of_year) {
    // Mean anomaly: fraction of orbit completed, scaled to radians
    double mean_anomaly = (TWO_PI * (double)day_of_year) / body->period_days;
    // Add the body's starting position at epoch
    double start_radians = body->start_angle_deg * PI_OVER_180;
    double position = mean_anomaly + start_radians;
    // Wrap to [0, 2π]
    while(position > TWO_PI)
        position -= TWO_PI;
    while(position < ZERO)
        position += TWO_PI;
    return position;
}

// calculate_hohmann_delta_v — How much propellant does the optimal transfer cost?
//
// Computes the total delta-v (km/s) for a Hohmann transfer between two
// circular orbits around the Sun. A Hohmann transfer is the most fuel-
// efficient two-burn trajectory: one burn to enter the elliptical transfer
// orbit, one burn to circularize at the destination.
//
// This is the BASE cost at perfect alignment. Actual cost may be higher
// if the planets aren't in the ideal phase (see calculate_transfer).
//
// Example scenarios a traveler might check:
//   Earth → Mars:    calculate_hohmann_delta_v(1.0, 1.524) ≈ 5.6 km/s
//   Earth → Venus:   calculate_hohmann_delta_v(1.0, 0.723) ≈ 5.2 km/s
//   Earth → Jupiter: calculate_hohmann_delta_v(1.0, 5.203) ≈ 8.8 km/s
//   Earth → Mercury: calculate_hohmann_delta_v(1.0, 0.387) ≈ 7.5 km/s
//
// For reference: the Saturn V could deliver about 8.5 km/s total.
// Mars is easy. Jupiter is right at the limit. Saturn (9.5 AU) is beyond
// chemical propulsion without gravity assists.
static double calculate_hohmann_delta_v(double r1_au, double r2_au) {
    double r1 = r1_au * AU_TO_KM * THOUSAND; // convert to meters
    double r2 = r2_au * AU_TO_KM * THOUSAND;

    // First burn: leave circular orbit at r1, enter transfer ellipse
    // v1 = circular velocity at r1
    // vt1 = velocity on transfer ellipse at r1 (vis-viva equation)
    double v1 = d_sqrt(MU_SUN / r1);
    double vt1 = d_sqrt(MU_SUN / r1) * d_sqrt(TWO * r2 / (r1 + r2));
    double delta_v1 = fabs(vt1 - v1);

    // Second burn: arrive at r2 on transfer ellipse, circularize
    double v2 = d_sqrt(MU_SUN / r2);
    double vt2 = d_sqrt(MU_SUN / r2) * d_sqrt(TWO * r1 / (r1 + r2));
    double delta_v2 = fabs(v2 - vt2);

    return (delta_v1 + delta_v2) / THOUSAND; // return in km/s
}

// calculate_transfer_time — How long will this trip take?
//
// Returns the flight time (days) for a Hohmann transfer. This is exactly
// half the orbital period of the transfer ellipse — you travel 180° around
// the Sun from departure to arrival.
//
// These times are non-negotiable with Hohmann transfers. Faster trajectories
// exist but cost exponentially more delta-v.
//
// Example transit times:
//   Earth → Mars:    ~259 days  (about 8.5 months)
//   Earth → Venus:   ~146 days  (about 5 months)
//   Earth → Jupiter: ~997 days  (about 2.7 years)
//   Earth → Saturn:  ~2190 days (about 6 years)
//   Earth → Moon:    3 days     (hardcoded, not Hohmann)
static double calculate_transfer_time(double r1_au, double r2_au) {
    double r1 = r1_au * AU_TO_KM * THOUSAND;
    double r2 = r2_au * AU_TO_KM * THOUSAND;
    double a_transfer = (r1 + r2) / TWO; // semi-major axis of transfer ellipse
    // Half-period: T/2 = π * √(a³/μ)
    double time_seconds = PI_CONST * d_sqrt(d_pow(a_transfer, THREE) / MU_SUN);
    return time_seconds / SECONDS_PER_DAY;
}

// calculate_ideal_phase — What angle should the destination be at for launch?
//
// For a Hohmann transfer, the destination must be at a specific angular
// position relative to Earth at the moment of departure. This is because
// the spacecraft takes a fixed amount of time to travel the transfer
// ellipse, and the destination planet keeps moving during that time.
// They need to meet at the arrival point.
//
// Positive phase = destination is ahead of Earth (counterclockwise).
// Negative phase = destination is behind Earth (clockwise).
//
// As a traveler, this is the number you're trying to match. When the
// "current phase" in the PHASE view matches the "ideal phase", that's
// your launch window. Long-press OK to jump there automatically.
//
// Real-world ideal phase angles:
//   Earth → Mars:    +44°  (Mars should be 44° ahead)
//   Earth → Venus:   -54°  (Venus should be 54° behind)
//   Earth → Jupiter: +97°  (Jupiter should be 97° ahead)
//   Earth → Mercury: -76°  (Mercury should be 76° behind)
static double calculate_ideal_phase(double r1_au, double r2_au, double dest_period_days) {
    double t_days = calculate_transfer_time(r1_au, r2_au);
    // How far the destination moves during the transfer
    double dest_angular_vel = THREE_SIXTY / dest_period_days; // degrees per day
    double dest_swept = dest_angular_vel * t_days;
    // The spacecraft arrives at the point 180° from departure.
    // The destination must be there when it arrives.
    // So at launch: destination should be at 180° minus what it will traverse.
    double ideal = ONE_EIGHTY - dest_swept;
    // Normalize to [-180°, +180°]
    while(ideal > ONE_EIGHTY)
        ideal -= THREE_SIXTY;
    while(ideal < -ONE_EIGHTY)
        ideal += THREE_SIXTY;
    return ideal;
}

// calculate_transfer — The main function. "Can I get there from here today?"
//
// Computes the full transfer picture for a given destination on a given day:
// delta-v cost, flight time, phase alignment, and whether it's even possible.
//
// This is what runs every time you scroll to a new day or destination.
// It combines all the orbital mechanics into a single actionable result.
//
// The delta-v penalty model:
//   - At ideal phase alignment (0° error): you pay base Hohmann delta-v
//   - At 45° error: ~1.19x base (still reasonable, "GOOD" window)
//   - At 90° error: ~1.75x base (expensive, "FAIR" window)
//   - At 135° error: ~2.69x base (very expensive, "POOR" window)
//   - At 180° error: 4x base (worst case, likely "NO PATH")
//   - Beyond 20 km/s total: marked as impossible
//
// Special cases:
//   - Earth's Moon: always possible, fixed 3.1 km/s / 3 days
//   - Outer moons (Europa, Titan, etc.): uses parent planet's orbit
//
// Example: You're on your Flipper, it's March 15, heading to Mars.
//   Mars is currently at +60° phase, ideal is +44°, error = 16°.
//   That's within "GOOD" window territory.
//   Delta-v: 5.6 * 1.024 = 5.7 km/s. Flight: 259 days.
//   Green light — start your pre-flight checks.
static TransferResult calculate_transfer(const CelestialBody* destination, int day_of_year) {
    TransferResult result = {0};

    double target_au = get_target_au(destination);

    // --- MOON SPECIAL CASE ---
    // The Moon doesn't need a Hohmann transfer — it's in Earth's gravity well.
    // Trans-lunar injection: ~3.1 km/s from LEO, ~3 day coast.
    // Always reachable regardless of phase. A quick hop.
    if(is_earth_moon(destination)) {
        double earth_angle = calculate_orbital_position(&earth, day_of_year);
        result.delta_v_total = THREE + (ONE / TEN); // 3.1 km/s
        result.flight_time_days = THREE;
        result.departure_angle = earth_angle * INV_PI_OVER_180;
        result.ideal_phase_deg = ZERO;
        result.current_phase_deg = ZERO;
        result.is_possible = true;
        return result;
    }

    // --- PLANETARY POSITIONS ---
    double earth_angle = calculate_orbital_position(&earth, day_of_year);
    double dest_angle;
    double dest_period;

    if(destination->is_moon) {
        // For outer moons: we care about the PARENT PLANET's position,
        // since that's what we're actually flying to.
        // Derive the parent's period from Kepler's 3rd law: T = T_earth * a^1.5
        CelestialBody parent_proxy = {
            "", ZERO, target_au, ZERO, ZERO, -1, false};
        static const double EARTH_PERIOD = 365.25;
        parent_proxy.period_days = EARTH_PERIOD * d_pow(target_au, ONE + HALF);
        dest_period = parent_proxy.period_days;
        parent_proxy.start_angle_deg = destination->start_angle_deg;
        dest_angle = calculate_orbital_position(&parent_proxy, day_of_year);
    } else {
        dest_angle = calculate_orbital_position(destination, day_of_year);
        dest_period = destination->period_days;
    }

    // --- PHASE ANGLE ---
    // The phase angle is how far ahead (+) or behind (-) the destination
    // is relative to Earth, measured in the direction of orbital motion.
    double phase_angle = dest_angle - earth_angle;
    while(phase_angle > PI_CONST)
        phase_angle -= TWO_PI;
    while(phase_angle < -PI_CONST)
        phase_angle += TWO_PI;

    double phase_deg = phase_angle * INV_PI_OVER_180;
    result.current_phase_deg = phase_deg;

    // --- IDEAL PHASE ---
    double ideal_deg = calculate_ideal_phase(earth.semi_major_au, target_au, dest_period);
    result.ideal_phase_deg = ideal_deg;

    // --- DELTA-V CALCULATION ---
    double base_dv = calculate_hohmann_delta_v(earth.semi_major_au, target_au);
    result.flight_time_days = calculate_transfer_time(earth.semi_major_au, target_au);

    // How far off are we from the ideal alignment?
    double phase_error_deg = fabs(phase_deg - ideal_deg);
    if(phase_error_deg > ONE_EIGHTY) phase_error_deg = THREE_SIXTY - phase_error_deg;

    // Quadratic penalty: bad alignment costs quadratically more fuel.
    // This models the reality that non-Hohmann trajectories (required when
    // phase is wrong) need progressively more energy as alignment worsens.
    double error_frac = phase_error_deg / ONE_EIGHTY; // normalize to 0..1
    double penalty = ONE + THREE * error_frac * error_frac;
    result.delta_v_total = base_dv * penalty;

    // --- DEPARTURE GEOMETRY ---
    result.departure_angle = earth_angle * INV_PI_OVER_180;
    while(result.departure_angle < ZERO)
        result.departure_angle += THREE_SIXTY;
    while(result.departure_angle >= THREE_SIXTY)
        result.departure_angle -= THREE_SIXTY;

    // --- FEASIBILITY ---
    // 20 km/s is roughly the limit of what chemical propulsion can deliver
    // from LEO (including gravity losses). Beyond that, you'd need nuclear
    // thermal, ion drives, or gravity assists — none of which this planner models.
    result.is_possible = (result.delta_v_total < TWENTY);

    return result;
}

