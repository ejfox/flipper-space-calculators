// draw_callback — Called by the Flipper GUI system whenever the screen
// needs to be redrawn. Dispatches to the appropriate view renderer.
static void draw_callback(Canvas* canvas, void* ctx) {
    AppState* state = (AppState*)ctx;
    switch(state->current_view) {
    case VIEW_ORBIT:
        draw_orbit_view(canvas, state);
        break;
    case VIEW_DATA:
        draw_data_view(canvas, state);
        break;
    case VIEW_PHASE:
        draw_phase_view(canvas, state);
        break;
    case VIEW_WINDOWS:
        draw_windows_view(canvas, state);
        break;
    default:
        break;
    }
}

// input_callback — Called by the Flipper input system when a button is pressed.
// Pushes the event into the message queue for the main loop to process.
static void input_callback(InputEvent* input_event, void* ctx) {
    furi_assert(ctx);
    FuriMessageQueue* event_queue = ctx;
    furi_message_queue_put(event_queue, input_event, FuriWaitForever);
}

// space_travel_calculator_app — Main entry point.
//
// Initializes state from the Flipper's real-time clock (so the app opens
// on today's date), sets up the GUI, and enters the event loop.
//
// The event loop handles:
//   - LONG OK:  find_next_window() — jump to optimal launch date
//   - SHORT OK: cycle views (Orbit → Data → Phase → Orbit)
//   - LEFT:     previous day (wraps Dec 31 → Jan 1)
//   - RIGHT:    next day (wraps Jan 1 → Dec 31)
//   - UP:       previous destination
//   - DOWN:     next destination
//   - BACK:     exit app
//
// Every navigation action recalculates the transfer for the new
// day/destination combination and triggers a screen redraw.
int32_t space_travel_calculator_app(void* p) {
    UNUSED(p);

    AppState* state = malloc(sizeof(AppState));
    furi_check(state != NULL);
    state->current_view = VIEW_ORBIT;
    state->current_destination = 0; // Start with Mars — the most popular destination
    state->needs_redraw = true;

    // Read today's date from the Flipper's hardware real-time clock
    // and convert to day-of-year (1-365) for the orbital calculations.
    {
        DateTime now;
        furi_hal_rtc_get_datetime(&now);
        static const int month_offsets[] = {0, 31, 59, 90, 120, 151, 181, 212, 243, 273, 304, 334};
        int doy = month_offsets[now.month - 1] + now.day;
        if(now.month > 2 && datetime_is_leap_year(now.year)) doy++;
        if(doy < 1) doy = 1;
        if(doy > 365) doy = 365;
        state->current_day = doy;
    }
    // Calculate the initial transfer for today → Mars
    state->current_transfer =
        calculate_transfer(&destinations[state->current_destination], state->current_day);

    // --- GUI SETUP ---
    Gui* gui = furi_record_open(RECORD_GUI);
    FuriMessageQueue* event_queue = furi_message_queue_alloc(8, sizeof(InputEvent));

    ViewPort* view_port = view_port_alloc();
    view_port_draw_callback_set(view_port, draw_callback, state);
    view_port_input_callback_set(view_port, input_callback, event_queue);
    gui_add_view_port(gui, view_port, GuiLayerFullscreen);

    // --- MAIN EVENT LOOP ---
    InputEvent event;
    bool running = true;

    while(running) {
        if(furi_message_queue_get(event_queue, &event, 100) == FuriStatusOk) {
            if(event.type == InputTypeLong && event.key == InputKeyOk) {
                // Long-press OK: "Find me the next launch window"
                state->current_day = find_next_window(
                    &destinations[state->current_destination], state->current_day);
                state->current_transfer = calculate_transfer(
                    &destinations[state->current_destination], state->current_day);
                state->needs_redraw = true;
            } else if(event.type == InputTypeShort && event.key == InputKeyOk) {
                // Short-press OK: cycle through the three views
                state->current_view = (ViewMode)((state->current_view + 1) % VIEW_COUNT);
                state->needs_redraw = true;
            } else if(event.type == InputTypePress || event.type == InputTypeRepeat) {
                switch(event.key) {
                case InputKeyBack:
                    running = false;
                    break;
                case InputKeyLeft:
                    state->current_day--;
                    if(state->current_day < 1) state->current_day = 365;
                    state->current_transfer = calculate_transfer(
                        &destinations[state->current_destination], state->current_day);
                    state->needs_redraw = true;
                    break;
                case InputKeyRight:
                    state->current_day++;
                    if(state->current_day > 365) state->current_day = 1;
                    state->current_transfer = calculate_transfer(
                        &destinations[state->current_destination], state->current_day);
                    state->needs_redraw = true;
                    break;
                case InputKeyUp:
                    state->current_destination--;
                    if(state->current_destination < 0)
                        state->current_destination = DESTINATION_COUNT - 1;
                    state->current_transfer = calculate_transfer(
                        &destinations[state->current_destination], state->current_day);
                    state->needs_redraw = true;
                    break;
                case InputKeyDown:
                    state->current_destination++;
                    if(state->current_destination >= DESTINATION_COUNT)
                        state->current_destination = 0;
                    state->current_transfer = calculate_transfer(
                        &destinations[state->current_destination], state->current_day);
                    state->needs_redraw = true;
                    break;
                default:
                    break;
                }
            }
        }

        if(state->needs_redraw) {
            view_port_update(view_port);
            state->needs_redraw = false;
        }
    }

    // --- CLEANUP ---
    gui_remove_view_port(gui, view_port);
    view_port_free(view_port);
    furi_message_queue_free(event_queue);
    furi_record_close(RECORD_GUI);
    free(state);

    return 0;
}
