// draw_data_view — Detailed mission parameters in text form.
//
// Shows everything a traveler needs before committing to a burn:
//   - LAUNCH date (calendar format)
//   - FLIGHT time (days, or years+days for long missions)
//   - DV: total delta-v budget needed (km/s)
//   - BURN: departure burn direction and angle
//   - WINDOW: quality rating based on phase alignment
//
// Window quality ratings:
//   OPTIMAL — Phase error < 10°. Launch now. This is the sweet spot.
//   GOOD    — Phase error < 45°. Reasonable cost, worth considering.
//   FAIR    — Phase error < 90°. Expensive but doable in an emergency.
//   POOR    — Phase error > 90°. Burning a lot of extra fuel. Wait if you can.
//
// When the transfer is impossible (>20 km/s), the view shows
// "IMPRACTICAL" and hints to long-press OK to find the next window.
//
// Example display for an optimal Mars window:
//   MARS
//   LAUNCH: MAY 27
//   FLIGHT: 259d
//   DV: 5.7 km/s
//   BURN: > 145
//   WINDOW: OPTIMAL
static void draw_data_view(Canvas* canvas, AppState* state) {
    canvas_clear(canvas);
    canvas_set_color(canvas, ColorBlack);

    canvas_set_font(canvas, FontPrimary);
    canvas_draw_str(canvas, 2, 10, destinations[state->current_destination].name);

    canvas_set_font(canvas, FontSecondary);

    if(state->current_transfer.is_possible) {
        char line[32];

        char date[12];
        format_date(date, sizeof(date), state->current_day);
        snprintf(line, sizeof(line), "LAUNCH: %s", date);
        canvas_draw_str(canvas, 2, 22, line);

        int days = (int)state->current_transfer.flight_time_days;
        if(days > 365) {
            snprintf(line, sizeof(line), "FLIGHT: %dy %dd", days / 365, days % 365);
        } else {
            snprintf(line, sizeof(line), "FLIGHT: %dd", days);
        }
        canvas_draw_str(canvas, 2, 32, line);

        snprintf(line, sizeof(line), "DV: %.1f km/s", state->current_transfer.delta_v_total);
        canvas_draw_str(canvas, 2, 42, line);

        const char* arrow = get_burn_arrow(state->current_transfer.departure_angle);
        snprintf(
            line, sizeof(line), "BURN: %s %.0f", arrow, state->current_transfer.departure_angle);
        canvas_draw_str(canvas, 2, 52, line);

        double err = fabs(state->current_transfer.current_phase_deg -
                          state->current_transfer.ideal_phase_deg);
        if(err > ONE_EIGHTY) err = THREE_SIXTY - err;
        if(err < TEN)
            canvas_draw_str(canvas, 2, 62, "WINDOW: OPTIMAL");
        else if(err < FORTY_FIVE)
            canvas_draw_str(canvas, 2, 62, "WINDOW: GOOD");
        else if(err < (FORTY_FIVE + FORTY_FIVE))
            canvas_draw_str(canvas, 2, 62, "WINDOW: FAIR");
        else
            canvas_draw_str(canvas, 2, 62, "WINDOW: POOR");
    } else {
        canvas_draw_str(canvas, 2, 30, "TRANSFER");
        canvas_draw_str(canvas, 2, 40, "IMPRACTICAL");
        canvas_draw_str(canvas, 2, 52, "LONG-PRESS OK");
        canvas_draw_str(canvas, 2, 62, "TO FIND WINDOW");
    }
}

