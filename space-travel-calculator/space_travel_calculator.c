// find_next_window — Scan forward to find the next optimal launch date.
//
// Starting from the day after start_day, scans through up to 365 days
// looking for the transfer window with the lowest delta-v. A "window"
// is a contiguous run of days where is_possible is true. Once it finds
// the first complete window (enters then exits), it returns the day
// with minimum delta-v within that window.
//
// Triggered by long-pressing OK on the Flipper.
//
// As a traveler: you're parked in orbit, the current alignment is garbage,
// and you want to know "when's my next shot?" Long-press OK and the
// app jumps straight to the cheapest day in the next window. Then you
// can fine-tune with LEFT/RIGHT if needed.
//
// For the Moon, this always returns start_day+1 (it's always reachable).
// For Mars, windows occur roughly every 26 months.
// For Venus, roughly every 19 months.
// For Jupiter, roughly every 13 months.
static int find_next_window(const CelestialBody* destination, int start_day) {
    int best_day = -1;
    double best_dv = 1e9;
    bool in_window = false;
    bool passed_window = false;

    for(int i = 1; i <= 365; i++) {
        int day = ((start_day - 1 + i) % 365) + 1; // wrap around year
        TransferResult tr = calculate_transfer(destination, day);
        if(tr.is_possible) {
            in_window = true;
            if(tr.delta_v_total < best_dv) {
                best_dv = tr.delta_v_total;
                best_day = day;
            }
        } else if(in_window) {
            // Just exited a window — we found our optimum
            passed_window = true;
            break;
        }
    }

    if(passed_window || in_window) return best_day;
    return start_day; // No window found within a year — stay put
}

// ============================================================================
// DRAWING — WINDOWS VIEW (Next 3 departure opportunities)
// ============================================================================

// A single upcoming departure opportunity.
typedef struct {
    int day;
    double delta_v;
    int flight_days;
} WindowInfo;

// find_windows — Find the next N optimal launch windows.
//
// For each window, finds the day with lowest delta-v across all 365 days,
// then excludes ±45 days around it so the next search finds a truly
// distinct window. Results are sorted chronologically from start_day.
//
// Think of this like checking departure boards at a spaceport:
//   "Next 3 flights to Mars departing from Earth..."
static int find_windows(
    const CelestialBody* destination,
    int start_day,
    WindowInfo* out,
    int max_windows) {
    int found = 0;
    int excluded[3] = {-1, -1, -1};

    for(int w = 0; w < max_windows; w++) {
        int best_day = -1;
        double best_dv = 1e9;
        int best_flight = 0;

        for(int i = 0; i < 365; i++) {
            int day = ((start_day - 1 + i) % 365) + 1;

            // Skip days within ±45 of a previously found window
            bool skip = false;
            for(int e = 0; e < found; e++) {
                int diff = day - excluded[e];
                if(diff < 0) diff = -diff;
                if(diff > 182) diff = 365 - diff;
                if(diff < 45) { skip = true; break; }
            }
            if(skip) continue;

            TransferResult tr = calculate_transfer(destination, day);
            if(tr.is_possible && tr.delta_v_total < best_dv) {
                best_dv = tr.delta_v_total;
                best_day = day;
                best_flight = (int)tr.flight_time_days;
            }
        }

        if(best_day < 0) break;

        excluded[found] = best_day;
        out[found].day = best_day;
        out[found].delta_v = best_dv;
        out[found].flight_days = best_flight;
        found++;
    }

    // Sort chronologically from start_day
    for(int i = 0; i < found - 1; i++) {
        for(int j = i + 1; j < found; j++) {
            int di = ((out[i].day - start_day + 365) % 365);
            int dj = ((out[j].day - start_day + 365) % 365);
            if(dj < di) {
                WindowInfo tmp = out[i];
                out[i] = out[j];
                out[j] = tmp;
            }
        }
    }

    return found;
}

// draw_windows_view — Departure board: next 3 launch opportunities.
//
// Displays like an airport/spaceport departure screen:
//
//   MARS — DEPARTURES
//   ─────────────────
//   1. MAY 27  5.7km/s  259d
//   2. AUG 14  6.1km/s  259d
//   3. NOV 02  8.3km/s  259d
//
// Each line shows the optimal launch date, fuel cost, and transit time.
// The first entry is always the next available window from today.
//
// For the Moon, all 3 will show ~3.1 km/s / 3d since it's always reachable.
// For outer planets, you might only get 1-2 windows within a year.
static void draw_windows_view(Canvas* canvas, AppState* state) {
    canvas_clear(canvas);
    canvas_set_color(canvas, ColorBlack);

    const CelestialBody* dest = &destinations[state->current_destination];

    // Header
    canvas_set_font(canvas, FontPrimary);
    canvas_draw_str(canvas, 2, 10, dest->name);

    canvas_set_font(canvas, FontSecondary);
    canvas_draw_str(canvas, 2, 20, "DEPARTURES");

    // Divider line
    canvas_draw_line(canvas, 2, 22, 126, 22);

    // Find next 3 windows
    WindowInfo windows[3];
    int count = find_windows(dest, state->current_day, windows, 3);

    if(count == 0) {
        canvas_draw_str(canvas, 2, 36, "NO WINDOWS FOUND");
        canvas_draw_str(canvas, 2, 48, "WITHIN 1 YEAR");
        return;
    }

    for(int i = 0; i < count; i++) {
        char line[40];
        char date[12];
        format_date(date, sizeof(date), windows[i].day);

        int flight = windows[i].flight_days;
        if(flight > 365) {
            snprintf(
                line,
                sizeof(line),
                "%d. %s %4.1f %dy%dd",
                i + 1,
                date,
                windows[i].delta_v,
                flight / 365,
                flight % 365);
        } else {
            snprintf(
                line,
                sizeof(line),
                "%d. %s %4.1fkm/s %dd",
                i + 1,
                date,
                windows[i].delta_v,
                flight);
        }
        canvas_draw_str(canvas, 2, 34 + i * 12, line);
    }
}

