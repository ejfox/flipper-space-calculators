// draw_moon_orbit_view — Geocentric view for Earth's Moon.
//
// When the traveler selects MOON, the orbit view switches from the
// heliocentric "Sun in the middle" diagram to a geocentric view:
// Earth at center, Moon orbiting around it, with a transfer line.
//
// This is physically correct — the Moon is gravitationally bound to Earth,
// not independently orbiting the Sun. A lunar transfer is an Earth-relative
// maneuver, not a heliocentric Hohmann transfer.
//
// The Moon's position updates as you scroll days, completing a full orbit
// every 27.3 days. You can watch it go around Earth in fast-forward.
static void draw_moon_orbit_view(Canvas* canvas, AppState* state) {
    const CelestialBody* dest = &destinations[state->current_destination];
    const int cx = 88; // Diagram center, right side of screen
    const int cy = 32;
    const int moon_orbit_r = 20; // Moon orbit radius in pixels

    // Earth at center — you are here
    canvas_draw_disc(canvas, cx, cy, 3);
    canvas_draw_str(canvas, cx - 3, cy - 6, "E");

    // Moon's orbital path
    canvas_draw_circle(canvas, cx, cy, moon_orbit_r);

    // Moon's current position
    double moon_angle = calculate_orbital_position(dest, state->current_day);
    int moon_x = cx + (int)((double)moon_orbit_r * d_cos(moon_angle));
    int moon_y = cy - (int)((double)moon_orbit_r * d_sin(moon_angle));
    canvas_draw_disc(canvas, moon_x, moon_y, 2);
    canvas_draw_str(canvas, moon_x - 2, moon_y - 4, "M");

    // Transfer path — direct line (lunar transfers are roughly straight)
    canvas_draw_line(canvas, cx, cy, moon_x, moon_y);
}

// draw_orbit_view — The main visual: heliocentric orbit diagram.
//
// This is the KSP-style transfer planner view. It shows:
//   - Sun at center (small filled dot)
//   - Earth's orbit (inner or outer circle depending on destination)
//   - Destination's orbit (scaled proportionally)
//   - Earth's position on its orbit (labeled "E")
//   - Destination's position on its orbit (labeled with first letter)
//   - Transfer arc (curved line approximating the Hohmann ellipse)
//   - Left sidebar: destination name, calendar date, delta-v, flight time
//
// Orbit scaling uses sqrt(AU) so that Jupiter (5.2 AU) doesn't push
// everything off-screen while Mercury (0.39 AU) remains visible.
// Minimum orbit separation is enforced so close orbits don't overlap.
//
// For moons of outer planets (Europa, Titan, etc.), the diagram shows
// the parent planet's orbit with the moon as a tiny dot offset from
// the parent. You're really flying to Jupiter — Europa is just your
// final stop after Jupiter orbit insertion.
//
// The transfer arc is drawn as a curved line with a slight outward
// (or inward) bulge to approximate the shape of a Hohmann ellipse.
// It's not geometrically exact, but it gives the right visual impression.
//
// Practical use: scroll LEFT/RIGHT and watch the planets move. When
// the transfer arc looks short and clean, you're near an optimal window.
// When it stretches or disappears, wait for better alignment.
static void draw_orbit_view(Canvas* canvas, AppState* state) {
    canvas_clear(canvas);
    canvas_set_color(canvas, ColorBlack);

    const CelestialBody* dest = &destinations[state->current_destination];

    if(is_earth_moon(dest)) {
        draw_moon_orbit_view(canvas, state);
    } else {
        const int cx = 88;
        const int cy = 32;

        double dest_au = get_target_au(dest);
        double max_au = (dest_au > ONE) ? dest_au : ONE;
        double scale = TWENTY_SIX / d_sqrt(max_au);
        int earth_r = (int)scale;
        int dest_r = (int)(scale * d_sqrt(dest_au));

        // Enforce minimum separation so orbits don't merge visually
        if(earth_r < 5) earth_r = 5;
        if(dest_r < 5) dest_r = 5;
        if(abs(dest_r - earth_r) < 4 && dest_au != ONE) {
            if(dest_au > ONE)
                dest_r = earth_r + 5;
            else
                dest_r = earth_r - 5;
            if(dest_r < 4) dest_r = 4;
        }

        // Sun — the thing everything orbits
        canvas_draw_disc(canvas, cx, cy, 2);

        // Orbital paths
        canvas_draw_circle(canvas, cx, cy, earth_r);
        canvas_draw_circle(canvas, cx, cy, dest_r);

        // Planet positions (updated in real-time as you scroll days)
        double earth_angle = calculate_orbital_position(&earth, state->current_day);
        double dest_angle = calculate_orbital_position(dest, state->current_day);
        // For moons, show the parent planet's position instead
        if(dest->is_moon) {
            static const double EARTH_PERIOD = 365.25;
            CelestialBody parent_proxy = {
                "", ZERO, dest_au, ZERO, dest->start_angle_deg, -1, false};
            parent_proxy.period_days = EARTH_PERIOD * d_pow(dest_au, ONE + HALF);
            dest_angle = calculate_orbital_position(&parent_proxy, state->current_day);
        }

        // Earth — your departure point
        int earth_x = cx + (int)((double)earth_r * d_cos(earth_angle));
        int earth_y = cy - (int)((double)earth_r * d_sin(earth_angle));
        canvas_draw_disc(canvas, earth_x, earth_y, 2);

        // Destination (or parent planet for moons)
        int dest_x = cx + (int)((double)dest_r * d_cos(dest_angle));
        int dest_y = cy - (int)((double)dest_r * d_sin(dest_angle));
        canvas_draw_disc(canvas, dest_x, dest_y, 2);

        // For moons: draw the actual moon as a tiny dot near its parent
        if(dest->is_moon) {
            double moon_angle = calculate_orbital_position(dest, state->current_day);
            int mx = dest_x + (int)(FOUR * d_cos(moon_angle));
            int my = dest_y - (int)(FOUR * d_sin(moon_angle));
            canvas_draw_disc(canvas, mx, my, 1);
        }

        // Transfer arc — the path your ship would take
        if(state->current_transfer.is_possible) {
            int steps = 12;
            int prev_x = earth_x;
            int prev_y = earth_y;
            for(int i = 1; i <= steps; i++) {
                double t = (double)i / (double)steps;
                double angle_diff = dest_angle - earth_angle;
                if(dest_au >= ONE) {
                    // Outer planet: transfer goes prograde (counterclockwise)
                    if(angle_diff <= ZERO) angle_diff += TWO_PI;
                    if(angle_diff > PI_CONST) angle_diff -= TWO_PI;
                } else {
                    // Inner planet: transfer goes retrograde
                    if(angle_diff >= ZERO) angle_diff -= TWO_PI;
                    if(angle_diff < -PI_CONST) angle_diff += TWO_PI;
                }
                double a = earth_angle + angle_diff * t;
                double r = (double)earth_r + ((double)dest_r - (double)earth_r) * t;
                // Bulge outward/inward to approximate elliptical shape
                double bulge = d_sin(t * PI_CONST) * THREE;
                if(dest_au >= ONE)
                    r += bulge;
                else
                    r -= bulge;
                int px = cx + (int)(r * d_cos(a));
                int py = cy - (int)(r * d_sin(a));
                canvas_draw_line(canvas, prev_x, prev_y, px, py);
                prev_x = px;
                prev_y = py;
            }
        }

        // Planet labels
        canvas_set_font(canvas, FontSecondary);
        canvas_draw_str(canvas, earth_x - 2, earth_y - 4, "E");
        char dl[2] = {dest->name[0], '\0'};
        canvas_draw_str(canvas, dest_x - 2, dest_y - 4, dl);
    }

    // --- LEFT SIDEBAR: at-a-glance mission data ---
    canvas_set_font(canvas, FontSecondary);
    canvas_draw_str(canvas, 0, 8, dest->name);

    char buf[24];
    format_date(buf, sizeof(buf), state->current_day);
    canvas_draw_str(canvas, 0, 18, buf);

    if(state->current_transfer.is_possible) {
        snprintf(buf, sizeof(buf), "%.1fkm/s", state->current_transfer.delta_v_total);
        canvas_draw_str(canvas, 0, 28, buf);
        snprintf(buf, sizeof(buf), "%dd", (int)state->current_transfer.flight_time_days);
        canvas_draw_str(canvas, 0, 38, buf);
    } else {
        canvas_draw_str(canvas, 0, 28, "NO PATH");
    }
}

