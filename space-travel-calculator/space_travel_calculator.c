// ============================================================================
// MATH WRAPPERS
// ============================================================================
// The ARM Cortex-M4 FPU is single-precision. The standard math functions
// (cos, sin, sqrt, pow) may return float, and mixing float with double
// literals triggers -Werror=double-promotion. These wrappers cast the
// return value to double explicitly.

static inline double d_cos(double x) {
    return (double)cos(x);
}
static inline double d_sin(double x) {
    return (double)sin(x);
}
static inline double d_sqrt(double x) {
    return (double)sqrt(x);
}
static inline double d_pow(double x, double y) {
    return (double)pow(x, y);
}

