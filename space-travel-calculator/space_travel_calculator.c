// get_target_au — Resolve the heliocentric orbit radius for transfer calculations.
//
// For planets, returns the planet's own semi-major axis.
// For moons, returns the PARENT PLANET's orbit radius, because that's what
// the Hohmann transfer actually targets. You fly to Jupiter's orbit, not
// to Europa's tiny 671,000 km loop around Jupiter.
//
// Example:
//   get_target_au(&mars)    → 1.524  (Mars's own orbit)
//   get_target_au(&europa)  → 5.203  (Jupiter's orbit)
//   get_target_au(&titan)   → 9.537  (Saturn's orbit)
//   get_target_au(&moon)    → 1.0    (Earth's orbit — special case)
static double get_target_au(const CelestialBody* body) {
    if(!body->is_moon) return body->semi_major_au;
    if(body->parent_body == 3) return JUPITER_AU;
    if(body->name[0] == 'T' && body->name[1] == 'I') return SATURN_AU;
    return EARTH_AU;
}

// is_earth_moon — Check if this destination is Earth's Moon.
//
// The Moon gets special treatment: it doesn't need a Hohmann transfer
// (it's gravitationally bound to Earth), it's always reachable, and
// the orbit view shows a geocentric diagram instead of heliocentric.
static bool is_earth_moon(const CelestialBody* body) {
    return body->is_moon && get_target_au(body) == EARTH_AU;
}

