// format_date — Convert day-of-year (1-365) to "MAY 30" style string.
//
// Used in all three views so the traveler sees calendar dates, not
// abstract day numbers. Because "launch on MAY 27" is more useful
// than "launch on day 147" when you're coordinating with ground crew.
static void format_date(char* buf, int buflen, int day_of_year) {
    static const char* month_names[] = {
        "JAN", "FEB", "MAR", "APR", "MAY", "JUN",
        "JUL", "AUG", "SEP", "OCT", "NOV", "DEC"};
    static const int month_days[] = {31, 28, 31, 30, 31, 30, 31, 31, 30, 31, 30, 31};
    int d = day_of_year;
    int m = 0;
    while(m < 11 && d > month_days[m]) {
        d -= month_days[m];
        m++;
    }
    snprintf(buf, (size_t)buflen, "%s %d", month_names[m], d);
}

// get_burn_arrow — Convert departure angle to an ASCII arrow character.
//
// Shows which direction to point your spacecraft for the departure burn.
// Uses 8 cardinal directions mapped to the departure angle.
// On the orbit view, this arrow appears next to Earth's position.
//
// Example: departure_angle = 135° → "\" (upper-left diagonal burn)
static const char* get_burn_arrow(double angle_degrees) {
    int octant = (int)((angle_degrees + TWENTY_TWO_HALF) / FORTY_FIVE) % 8;
    static const char* arrows[] = {">", "/", "^", "\\", "<", "/", "v", "\\"};
    return arrows[octant];
}

