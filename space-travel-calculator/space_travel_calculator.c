static const double MU_SUN = 1.32712440018e20; // Sun's gravitational parameter (m^3/s^2)
static const double AU_TO_KM = 149597870.7; // 1 Astronomical Unit in kilometers
static const double SECONDS_PER_DAY = 86400.0;
static const double PI_CONST = 3.14159265358979323846;
static const double TWO_PI = 6.28318530717958647692;
static const double PI_OVER_180 = 0.01745329251994329577; // degrees → radians
