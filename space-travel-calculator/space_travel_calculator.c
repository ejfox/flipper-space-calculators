typedef enum {
    VIEW_ORBIT,
    VIEW_DATA,
    VIEW_PHASE,
    VIEW_WINDOWS,
    VIEW_COUNT
} ViewMode;

// A celestial body the traveler can navigate to.
//
// For planets, semi_major_au is the heliocentric orbit radius.
// For moons, semi_major_au is the moon's distance from its parent planet
// (in AU), and parent_body indexes into the destinations array to identify
// the parent. The actual transfer targets the parent planet's orbit.
//
// Example: Europa has semi_major_au = 0.00449 AU (from Jupiter), but
// the transfer calculation uses Jupiter's 5.203 AU heliocentric orbit.
typedef struct {
    const char* name;
    double period_days; // Orbital period around parent (days)
    double semi_major_au; // Orbit radius (AU from Sun, or AU from parent for moons)
    double eccentricity; // Orbital eccentricity (0 = circle, 1 = parabola)
    double start_angle_deg; // Position at epoch (approx Jan 1 2024, degrees)
    int parent_body; // -1 for planets/Earth's moon, array index for outer moons
    bool is_moon;
} CelestialBody;

// The result of computing a transfer to a destination on a given day.
//
// Example for an Earth→Mars transfer on an optimal day:
//   delta_v_total    = 5.6 km/s  (total propellant cost, both burns)
//   flight_time_days = 259       (about 8.5 months in transit)
//   departure_angle  = 145.0     (burn direction in degrees)
//   ideal_phase_deg  = +44       (Mars should be 44° ahead of Earth)
//   current_phase_deg = +42      (Mars is currently 42° ahead — close!)
//   is_possible      = true      (delta-v < 20 km/s cutoff)
typedef struct {
    double delta_v_total; // Total delta-v for both burns (km/s)
    double flight_time_days; // Time in transit (days)
    double departure_angle; // Direction of departure burn (degrees, 0=right)
    double ideal_phase_deg; // Where the destination SHOULD be relative to Earth
    double current_phase_deg; // Where the destination IS relative to Earth
    bool is_possible; // true if delta-v < 20 km/s (within chemical propulsion)
} TransferResult;

